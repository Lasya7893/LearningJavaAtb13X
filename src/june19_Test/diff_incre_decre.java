package june19_Test;

public class diff_incre_decre {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(++a);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(b--);
        System.out.println(b);
        System.out.println(--b);
    }
}
